# Peça um número ao usuário e exiba:
# O dobro
# O triplo
# A raiz quadrada

number = int(input("Enter the number: "))

double = number * 2
triple = number * 3
raiz = number ** 0.5

print("Double the number ", number, " is: ", double)
print("Triple the number ", number, " is: ", triple)
print("square root the number", number, " is: ", raiz)